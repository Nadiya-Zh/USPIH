s1 = float(input("Введіть відстань, яку проходить Василь пішки (км): "))
v1 = float(input("Введіть швидкість Василя (км/год): "))
s2 = float(input("Введіть відстань, яку проїжджає автобус (км): "))
v2 = float(input("Введіть швидкість автобуса (км/год): "))
t1 = s1 / v1
t2 = s2 / v2
t = t1 + t2
print("Час у дорозі від дому до стадіону:", t, "год")
